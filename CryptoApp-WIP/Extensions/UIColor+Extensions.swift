import UIKit

extension UIColor {
    
    public class var greenColor: UIColor {
        return UIColor.init(red: 55 / 255, green: 171 / 255, blue: 47 / 255, alpha: 1.0)
    }
    
    public class var redColor: UIColor {
        return UIColor.init(red: 199 / 255, green: 21 / 255, blue: 42 / 255, alpha: 1.0)
    }
    
    public class var grayColor: UIColor {
        return UIColor.init(red: 151 / 255, green: 151 / 255, blue: 151 / 255, alpha: 1.0)
    }
    
    public class var separatorColor: UIColor {
        return UIColor.init(red: 242 / 255, green: 244 / 255, blue: 245 / 255, alpha: 1.0)
    }
    
}
